        if reload:
            console.print("[bold yellow]🔄 Hot-reload enabled[/bold yellow]")
            os.environ["CLAW_FORGE_DB_URL"] = db_url
            os.environ["CLAW_FORGE_PROJECT_PATH"] = str(project_path)
