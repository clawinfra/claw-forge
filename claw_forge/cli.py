@app.command()
def run(
    config: str = typer.Option(
        "claw-forge.yaml", "--config", "-c",
        help="Path to claw-forge.yaml config file.",
    ),
    project: str = typer.Option(".", "--project", "-p", help="Project directory."),
    task: str = typer.Option(
        "coding", "--task", "-t",
        help="Agent task type: coding | testing | review.",
    ),
    model: str = typer.Option(
        "claude-sonnet-4-6", "--model", "-m",
        help=(
            "Model to use. Supported formats:\n"
            "  claude-sonnet-4-6             bare model (pool picks provider)\n"
            "  anthropic-proxy-1/claude-opus-4-6    pin to specific provider\n"
            "  sonnet                               alias from model_aliases in config\n"
            "Defaults to the value in claw-forge.yaml."
        ),
    ),
    concurrency: int = typer.Option(
        5, "--concurrency", "-n",
        help="Max parallel coding agents (1–10). Higher = faster but more API spend.",
    ),
    yolo: bool = typer.Option(
        False, "--yolo/--no-yolo",
        help="YOLO mode: skip human-input gates, max concurrency, aggressive retry on errors.",
    ),
    dry_run: bool = typer.Option(
        False, "--dry-run",
        help="Print tasks in dependency order without executing.",
    ),
    edit_mode: str = typer.Option(
        "str_replace", "--edit-mode",
        help=(
            "Edit tool mode for agent file operations.\n"
            "  str_replace: current default (exact text matching)\n"
            "  hashline: content-addressed line tagging (better for weak models)\n"
            "[default: str_replace]"
        ),
    ),
    loop_detect_threshold: int = typer.Option(
        5,
        "--loop-detect-threshold",
        help=(
            "Max edits to a single file before injecting a 'reconsider' prompt (0=disable). "
            "When --edit-mode hashline is active, threshold is auto-raised by 3 "
            "(e.g. default 5 → 8). "
            "[default: 5]"
        ),
    ),
) -> None:
    """Run agents on a project until all features pass.

    Examples:

        # Run with defaults (reads claw-forge.yaml)
        claw-forge run

        # Run with 3 parallel agents
        claw-forge run --concurrency 3

        # Run with a specific model
        claw-forge run --model claude-opus-4-6 --concurrency 2

        # YOLO mode — no human-input gates, max speed
        claw-forge run --yolo

        # Dry-run — see what would execute
        claw-forge run --dry-run
    """
    import asyncio

    from sqlalchemy.ext.asyncio import AsyncSession, async_sessionmaker, create_async_engine

    from claw_forge.orchestrator.dispatcher import Dispatcher
    from claw_forge.state.models import Base, Task
    from claw_forge.state.models import Session as DbSession
    from claw_forge.state.scheduler import TaskNode

    # Resolve config path: prefer project-relative if config is a bare filename
    _config_path = Path(config)
    if not _config_path.is_absolute() and not _config_path.exists():
        _project_config = Path(project) / config
        if _project_config.exists():
            config = str(_project_config)
    # Validate edit_mode
    _valid_edit_modes = ("str_replace", "hashline")
    if edit_mode not in _valid_edit_modes:
        console.print(
            f"[red]Invalid --edit-mode '{edit_mode}'. "
            f"Choose from: {', '.join(_valid_edit_modes)}[/red]"
        )
        raise typer.Exit(1) from None

    # Allow config file to set edit_mode (CLI flag takes priority)
    cfg = _load_config(config)
    _config_edit_mode = cfg.get("agent", {}).get("edit_mode", "str_replace")
    if edit_mode == "str_replace" and _config_edit_mode == "hashline":
        edit_mode = _config_edit_mode

    # Allow config to set loop_detect_threshold (CLI flag takes priority)
    _config_threshold = cfg.get("agent", {}).get("loop_detect_threshold", 5)
    if loop_detect_threshold == 5 and _config_threshold != 5:
        loop_detect_threshold = int(_config_threshold)

    if loop_detect_threshold < 0:
        console.print("[red]--loop-detect-threshold must be ≥ 0[/red]")
        raise typer.Exit(1) from None

    resolved = resolve_model(model, cfg)
    if resolved.alias_resolved:
        console.print(f"[dim]Model alias '{model}' → {resolved.model_id}[/dim]")
    if resolved.provider_hint:
        console.print(f"[dim]Provider pinned: {resolved.provider_hint}[/dim]")
    model = resolved.model_id
    console.print(f"[bold]claw-forge[/bold] v{__version__}")
    console.print(f"Project: {project}")
    console.print(f"Task: {task}")
    console.print(f"Model: {model}")
    if edit_mode == "hashline":
        console.print(f"Edit mode: [cyan]{edit_mode}[/cyan]  (content-addressed line tagging)")
    console.print(f"Providers: {len(cfg.get('providers', {}))}")
    if shutil.which("claude"):
        console.print("Executor:  claude CLI (autonomous agent — writes files) ✅")
    else:
        console.print(
            "[yellow]Executor:  API-only mode (no claude CLI in PATH — "
            "LLM responses only, files NOT written)[/yellow]"
        )
        console.print(
            "[dim]  → Install claude CLI for full autonomous coding: "
            "https://claude.ai/download[/dim]"
        )

    if yolo:
        cpu_count = max(1, os.cpu_count() or 4)
        console.print(
            f"[bold yellow]⚠️  YOLO MODE: Human approval skipped, max concurrency ({cpu_count}), aggressive retry[/bold yellow]"  # noqa: E501
        )

    # Resolve project path and create .claw-forge directory
    project_path = Path(project).resolve()

    # Guard: prevent running agents against the claw-forge source tree itself.
    # Detect by checking whether project_path contains claw_forge/cli.py — if so,
    # the user is pointing the orchestrator at its own repo, which would cause agents
    # to commit generated app code into claw-forge.
    _self_marker = Path(__file__).resolve()
    if (_self_marker.parent.parent == project_path) or project_path in _self_marker.parents:
        console.print(
            "[bold red]ERROR:[/bold red] The project path resolves to the claw-forge "
            "source directory itself.\n"
            "Running agents here would commit generated code into the claw-forge repo.\n"
            f"  project_path : {project_path}\n"
            f"  claw-forge   : {_self_marker.parent.parent}\n\n"
            "Point --project at your target application directory instead."
        )
        raise SystemExit(1)

    claw_forge_dir = project_path / ".claw-forge"
    claw_forge_dir.mkdir(parents=True, exist_ok=True)
    db_path = claw_forge_dir / "state.db"
    db_url = f"sqlite+aiosqlite:///{db_path}"
    console.print(f"[dim]DB: {db_path}[/dim]")

    # Auto-start state service (project-aware: restarts if serving a different project)
    _state_port = cfg.get("state", {}).get("port", 8420)
    _state_port = _ensure_state_service(project_path, _state_port)
    console.print(f"[dim]State service on port {_state_port}[/dim]")

    # Set up git tracking
    from claw_forge.git import GitOps

    git_cfg = cfg.get("git", {})
    git_enabled = git_cfg.get("enabled", True)
    git_merge_strategy = git_cfg.get("merge_strategy", "auto")
    git_branch_prefix = git_cfg.get("branch_prefix", "feat")
    git_commit_on_boundary = git_cfg.get("commit_on_plugin_boundary", True)
    git_ops = GitOps(project_dir=project_path, enabled=git_enabled)

    # Set up async engine
    engine = create_async_engine(db_url, echo=False)
    async_session_maker = async_sessionmaker(
        engine, class_=AsyncSession, expire_on_commit=False
    )

    async def main() -> None:
        async with engine.begin() as conn:
            await conn.run_sync(Base.metadata.create_all)
        await _migrate_schema(engine)

        async with async_session_maker() as session:
            # Find or create a session for this project
            from sqlalchemy import select

            stmt = (
                select(DbSession)
                .where(DbSession.project_path == str(project_path))
                .where(
                    (DbSession.status == "running") | (DbSession.status == "pending")
                )
                .order_by(DbSession.created_at.desc())
                .limit(1)
            )
            result = await session.execute(stmt)
            db_session = result.scalar_one_or_none()

            if db_session is None:
                # No existing session — create one
                import uuid

                db_session = DbSession(
                    id=str(uuid.uuid4()),
                    project_path=str(project_path),
                    status="pending",
                )
                session.add(db_session)
                await session.commit()
                await session.refresh(db_session)

            # Fetch pending, failed, or orphaned running tasks (retry)
            # "running" tasks are orphaned from a previous interrupted run — treat as pending
            stmt_tasks = (
                select(Task)
                .where(Task.session_id == db_session.id)
                .where(
                    (Task.status == "pending")
                    | (Task.status == "failed")
                    | (Task.status == "running")
                )
            )
            result_tasks = await session.execute(stmt_tasks)
            tasks = result_tasks.scalars().all()

            if not tasks:
                console.print(
                    "[yellow]No pending tasks found — run 'claw-forge plan <spec>' first[/yellow]"
                )
                return

            # Reset any orphaned "running" tasks back to "pending" in the DB
            # so the UI shows the correct state before agents re-execute them
            orphaned = [t for t in tasks if t.status == "running"]
            if orphaned:
                console.print(
                    f"[yellow]Resetting {len(orphaned)} orphaned task(s) "
                    "from previous interrupted run[/yellow]"
                )
                for t in orphaned:
                    t.status = "pending"
                    t.started_at = None
                await session.commit()

            # Build TaskNode objects from DB tasks
            task_nodes: list[TaskNode] = []
            for t in tasks:
                depends_on: list[str] = t.depends_on or []
                node = TaskNode(
                    id=t.id,
                    plugin_name=t.plugin_name,
                    priority=t.priority,
                    depends_on=depends_on,
                    status="pending",  # reset failed/orphaned tasks to pending for retry
                    category=t.category or "",
                    steps=t.steps or [],
                )
                task_nodes.append(node)

            # Dry-run: print tasks and exit
            if dry_run:
                from claw_forge.state.scheduler import Scheduler

                scheduler = Scheduler()
                for node in task_nodes:
                    scheduler.add_task(node)
                waves = scheduler.get_execution_order()

                console.print(f"\n[bold]Execution plan ({len(waves)} waves):[/bold]\n")
                for wave_idx, wave in enumerate(waves, 1):
                    console.print(f"  Wave {wave_idx}:")
                    for task_id in wave:
                        task = next((t for t in tasks if t.id == task_id), None)
                        if task:
                            desc = task.description or task.plugin_name
                            console.print(f"    • {task_id[:8]}…  {desc}")
                console.print(
                    f"\n[dim]To run: claw-forge run --project {project_path}[/dim]\n"
                    f"[dim]Board:  claw-forge ui  --project {project_path}[/dim]"
                )
                return

            # Build provider pool for direct API fallback
            from claw_forge.pool.manager import ProviderPoolManager

            configs = load_configs_from_yaml(cfg)
            pool = ProviderPoolManager(configs) if configs else None

            # Lock to serialise env-sensitive options construction.
            # Matches the _env_client_lock pattern from autonomous-coding:
            # env reads + ClaudeAgentOptions creation happen under lock;
            # AgentSession connect + agent execution run fully in parallel.
            # The dispatcher's Semaphore(max_concurrency) gates overall
            # concurrency; this lock only prevents env-read races.
            _env_lock = asyncio.Lock()

            # Build task handler — communicates with the state service via HTTP
            # so that WebSocket events are broadcast to the Kanban UI.
            _state_base = f"http://127.0.0.1:{_state_port}"

            async def _patch_task(
                client: httpx.AsyncClient, task_id: str, **fields: Any,
            ) -> None:
                """PATCH task status via HTTP (triggers WS broadcast)."""
                with suppress(httpx.HTTPError):
                    await client.patch(
                        f"{_state_base}/tasks/{task_id}",
                        json=fields, timeout=5,
                    )

            def _short_name(desc: str) -> str:
                """Extract a short label from a task description.

                Strips common prefixes like "Task X:" or "Feature N:" and
                truncates to 40 chars so the activity log stays scannable.
                """
                import re

                # Remove "Task <word>: " or "Feature <word>: " prefix
                cleaned = re.sub(
                    r"^(?:task|feature)\s+\S+:\s*",
                    "",
                    desc,
                    flags=re.IGNORECASE,
                )
                if len(cleaned) > 40:
                    return cleaned[:37] + "…"
                return cleaned

            def _first_line(text: str, max_len: int = 120) -> str:
                """Return the first non-empty line, truncated."""
                line = text.strip().split("\n", 1)[0].strip()
                if len(line) > max_len:
                    return line[:max_len - 1] + "…"
                return line

            # Map tool names to the input key that best describes what they do
            _TOOL_KEY: dict[str, str] = {
                "Write": "file_path", "Edit": "file_path",
                "Read": "file_path", "Bash": "command",
                "Glob": "pattern", "Grep": "pattern",
            }

            def _fmt_tool(name: str, raw: object) -> str:
                """Format tool input into a short human-readable string."""
                if isinstance(raw, dict):
                    key = _TOOL_KEY.get(name)
                    if key and key in raw:
                        val = str(raw[key])
                        if name == "Bash":
                            val = val[:80]
                        return val
                return str(raw)[:80]

            # Dedup: track all logged (role, content) per task to avoid
            # duplicates from SDK yielding streaming + complete messages.
            _seen_logs: dict[str, set[tuple[str, str]]] = {}

            async def _log_agent(
                client: httpx.AsyncClient,
                task_id: str,
                task_name: str,
                role: str,
                content: str,
                level: str = "info",
                model: str = "",
            ) -> None:
                """POST agent log entry via HTTP (triggers WS broadcast)."""
                short = _first_line(content)
                # Skip any duplicate (same role + content already seen for this task)
                key = (role, short)
                seen = _seen_logs.setdefault(task_id, set())
                if key in seen:
                    return
                seen.add(key)
                with suppress(httpx.HTTPError):
                    await client.post(
                        f"{_state_base}/tasks/{task_id}/agent-log",
                        json={
                            "role": role,
                            "content": short,
                            "task_name": _short_name(task_name),
                            "level": level,
                            "model": model or None,
                        },
                        timeout=5,
                    )

            async def task_handler(task_node: TaskNode) -> dict[str, Any]:
                async with httpx.AsyncClient() as http:
                    # Mark task as running in DB + notify UI via HTTP
                    async with async_session_maker() as task_session:
                        stmt_update = select(Task).where(Task.id == task_node.id)
                        result = await task_session.execute(stmt_update)
                        db_task = result.scalar_one()
                        task_name = db_task.description or db_task.plugin_name
                        prompt = db_task.description or f"Execute task: {db_task.plugin_name}"
                        if db_task.steps:
                            numbered = "\n".join(
                                f"{i + 1}. {s}" for i, s in enumerate(db_task.steps)
                            )
                            prompt = f"{prompt}\n\n## Verification Steps\n{numbered}"
                        db_task.status = "running"
                        db_task.started_at = datetime.now(UTC)
                        await task_session.commit()

                    # Notify UI via state service HTTP (best-effort)
                    await _patch_task(http, task_node.id, status="running")

                    # Create a feature branch for this task (best-effort — git
                    # errors must not crash the dispatcher or leave tasks stuck).
                    import logging as _logging
                    import re as _re

                    _slug = _re.sub(
                        r"[^a-z0-9]+", "-",
                        (task_node.plugin_name + "-" + task_node.id[:8]).lower(),
                    ).strip("-")
                    if git_enabled:
                        try:
                            await git_ops.create_branch(
                                task_node.id, _slug, prefix=git_branch_prefix,
                            )
                        except Exception as _git_branch_err:
                            _logging.getLogger(__name__).warning(
                                "Git branch creation failed for task %s (continuing): %s",
                                task_node.id, _git_branch_err,
                            )

                    output = ""
                    success = False
                    _cancelled = False

                    try:
                        # ── Option 1: claude_agent_sdk + claude CLI (full autonomous agent) ──
                        # claude_agent_sdk is a declared dependency so it's always installed.
                        # It requires the `claude` CLI in PATH to actually run agents.
                        sdk_available = (
                            shutil.which("claude") is not None
                        )

                        if sdk_available:
                            from claude_agent_sdk import ClaudeAgentOptions

                            from claw_forge.agent.hooks import get_default_hooks as _ghooks
                            from claw_forge.agent.session import AgentSession

                            # UNDER LOCK: env reads + options construction
                            # (matches autonomous-coding _env_client_lock pattern)
                            async with _env_lock:
                                sdk_env: dict[str, str] = {}
                                _api_key = os.environ.get("ANTHROPIC_API_KEY", "")
                                if not _api_key and pool is not None:
                                    _provs = pool.providers
                                    if _provs and hasattr(_provs[0], "config"):
                                        _api_key = _provs[0].config.api_key or ""
                                if _api_key:
                                    sdk_env["ANTHROPIC_API_KEY"] = _api_key
                                elif not _api_key:
                                    _oauth_tok = os.environ.get("ANTHROPIC_SETUP_TOKEN", "")
                                    if not _oauth_tok and pool is not None:
                                        _provs = pool.providers
                                        if _provs and hasattr(_provs[0], "config"):
                                            _cfg = _provs[0].config
                                            _oauth_tok = getattr(_cfg, "oauth_token", "") or ""
                                    if _oauth_tok:
                                        sdk_env["ANTHROPIC_SETUP_TOKEN"] = _oauth_tok

                                _hooks = _ghooks(
                                    edit_mode=edit_mode,
                                    loop_detect_threshold=loop_detect_threshold,
                                )

                                options = ClaudeAgentOptions(
                                    model=model,
                                    cwd=str(project_path),
                                    env=sdk_env,
                                    permission_mode="bypassPermissions",
                                    hooks=_hooks,
                                )

                            # OUTSIDE LOCK: connect + run (parallel with other agents)
                            full_output: list[str] = []
                            try:
                                async with AgentSession(options) as agent_session:
                                    async for msg in agent_session.run(prompt):  # noqa: E501
                                        _cls = type(msg).__name__
                                        if _cls == "ResultMessage":
                                            _result = getattr(msg, "result", None)
                                            if _result:
                                                full_output.append(str(_result))
                                                await _log_agent(
                                                    http, task_node.id, task_name,
                                                    "result", str(_result)[:500],
                                                    model=model,
                                                )
                                            continue
                                        if _cls != "AssistantMessage":
                                            continue
                                        content = getattr(msg, "content", None)
                                        if not isinstance(content, list):
                                            continue
                                        for block in content:
                                            _bcls = type(block).__name__
                                            if _bcls == "TextBlock":
                                                full_output.append(block.text)
                                                await _log_agent(
                                                    http, task_node.id, task_name,
                                                    "assistant", block.text[:500],
                                                    model=model,
                                                )
                                            elif _bcls == "ToolUseBlock":
                                                _tn = getattr(block, "name", "?")
                                                _raw = getattr(block, "input", {})
                                                _ti = _fmt_tool(_tn, _raw)
                                                await _log_agent(
                                                    http, task_node.id, task_name,
                                                    "tool_use", f"{_tn} → {_ti}",
                                                    model=model,
                                                )
                                            elif _bcls == "ToolResultBlock":
                                                _is_err = getattr(block, "is_error", False)
                                                _tc = str(getattr(block, "content", ""))[:300]
                                                await _log_agent(
                                                    http, task_node.id, task_name,
                                                    "tool_result", _tc,
                                                    level="error" if _is_err else "info",
                                                    model=model,
                                                )
                                        _err = getattr(msg, "error", None)
                                        if _err:
                                            raise RuntimeError(
                                                f"Agent error: {_err} — "
                                                "check `claude login` or verify API key in .env"
                                            )
                                output = "\n".join(full_output)
                                if not output.strip():
                                    raise RuntimeError(
                                        "Agent produced no output — "
                                        "check `claude login` or verify API key in .env"
                                    )
                                success = True
                            except Exception as sdk_exc:
                                output = str(sdk_exc)
                                success = False
                                await _log_agent(
                                    http, task_node.id, task_name,
                                    "error", str(sdk_exc)[:500],
                                    level="error",
                                    model=model,
                                )

                        # ── Option 2: direct API call via provider pool ───────────────
                        # Used when claude CLI is not installed (API-only mode).
                        # BUG-11 fix: parse code blocks from LLM output and write
                        # them to the project directory.
                        elif pool is not None:
                            system_prompt = (
                                "You are an expert software engineer. "
                                "Implement the requested feature precisely and completely. "
                                "Return code in fenced blocks with the filename as the "
                                "info string, e.g.:\n"
                                "```path/to/file.py\n<code>\n```\n\n"
                                f"Project directory: {project_path}"
                            )
                            await _log_agent(
                                http, task_node.id, task_name,
                                "assistant", "Sending request to provider pool…",
                                model=model,
                            )
                            task_complexity = _PLUGIN_COMPLEXITY.get(
                                db_task.plugin_name or "", "medium"
                            )
                            response = await pool.execute(
                                model=model,
                                messages=[{"role": "user", "content": prompt}],
                                system=system_prompt,
                                max_tokens=8192,
                                complexity=task_complexity,
                            )
                            output = response.content or ""
                            await _log_agent(
                                http, task_node.id, task_name,
                                "result", output[:500],
                                model=model,
                            )

                            # Write any code blocks to disk (BUG-11)
                            from claw_forge.output_parser import write_code_blocks

                            written_files = write_code_blocks(
                                output, project_path
                            )
                            if written_files:
                                output += (
                                    f"\n\n--- Files written ({len(written_files)}) ---\n"
                                    + "\n".join(f"  • {f}" for f in written_files)
                                )
                            success = True

                        # ── No executor available ─────────────────────────────────────
                        else:
                            output = (
                                "No agent executor available.\n"
                                "  • For full autonomous coding: install the claude CLI "
                                "(https://claude.ai/download) and ensure it is in PATH\n"
                                "  • For API-only mode: add a provider with a valid API key "
                                "to claw-forge.yaml"
                            )
                            success = False

                    except asyncio.CancelledError:
                        _cancelled = True
                        raise

                    except Exception as exc:
                        output = str(exc)
                        success = False

                    finally:
                        if _cancelled:
                            # Stop-all only pauses tasks that were "running" in the DB.
                            # Tasks that raced from pending → running (acquired the
                            # semaphore after stop-all committed) are still "running".
                            # Reset those to "pending" so they don't appear as active.
                            try:
                                async with async_session_maker() as cancel_session:
                                    stmt_c = select(Task).where(Task.id == task_node.id)
                                    res_c = await cancel_session.execute(stmt_c)
                                    c_task = res_c.scalar_one()
                                    if c_task.status == "running":
                                        c_task.status = "pending"
                                        c_task.started_at = None
                                        await cancel_session.commit()
                                        await _patch_task(
                                            http, task_node.id, status="pending",
                                        )
                            except Exception:
                                pass  # best-effort cleanup
                            return {"success": False, "output": "cancelled"}  # noqa: B012
                        # Persist final status directly to DB
                        async with async_session_maker() as fin_session:
                            stmt_fin = select(Task).where(Task.id == task_node.id)
                            res_fin = await fin_session.execute(stmt_fin)
                            fin_task = res_fin.scalar_one()
                            fin_task.status = "completed" if success else "failed"
                            fin_task.completed_at = datetime.now(UTC)
                            fin_task.error_message = None if success else output
                            fin_task.result_json = {"output": output} if success else None
                            await fin_session.commit()

                        # Git: checkpoint + optional merge on success
                        if git_enabled and success and git_commit_on_boundary:
                            await git_ops.checkpoint(
                                message=f"{task_node.plugin_name}({_slug}): completed",
                                task_id=task_node.id,
                                plugin=task_node.plugin_name,
                                phase=task_node.plugin_name,
                                session_id=db_session.id,
                            )
                            if git_merge_strategy == "auto":
                                _branch_name = f"{git_branch_prefix}/{_slug}"
                                await git_ops.merge(_branch_name)

                        # Notify UI via state service HTTP (best-effort)
                        await _patch_task(
                            http, task_node.id,
                            status="completed" if success else "failed",
                            **({"error_message": output} if not success else {}),
                        )

                return {"success": success, "output": output}

            # Run dispatcher with pause/resume re-dispatch loop.
            # When Stop All pauses the dispatcher, we wait for Resume All,
            # then re-fetch remaining tasks from the DB and re-dispatch.
            total_completed: dict[str, dict[str, Any]] = {}
            total_failed: dict[str, str] = {}

            current_nodes = task_nodes
            while current_nodes:
                dispatcher = Dispatcher(
                    handler=task_handler,
                    max_concurrency=concurrency,
                    yolo=yolo,
                    state_url=_state_base,
                )
                for node in current_nodes:
                    dispatcher.add_task(node)

                dispatch_result = await dispatcher.run()
                total_completed.update(dispatch_result.completed)
                total_failed.update(dispatch_result.failed)

                if not dispatcher.is_paused:
                    break  # Normal completion — all waves done

                # ── Paused: wait for Resume All ────────────────────────────
                console.print(
                    "\n[bold yellow]⏸  Paused — waiting for Resume All…[/bold yellow]"
                )
                async with httpx.AsyncClient() as poll_client:
                    while True:
                        await asyncio.sleep(1)
                        try:
                            resp = await poll_client.get(
                                f"{_state_base}/project/paused"
                                f"?session_id={db_session.id}",
                                timeout=3.0,
                            )
                            if not resp.json().get("paused", True):
                                break
                        except Exception:  # noqa: BLE001
                            pass
                console.print(
                    "[bold green]▶  Resumed — re-dispatching tasks…[/bold green]"
                )

                # Re-fetch pending/paused tasks from DB for the next cycle
                _seen_logs.clear()
                async with async_session_maker() as resume_session:
                    stmt_resume = select(Task).where(
                        Task.session_id == db_session.id,
                        Task.status.in_(["pending", "paused"]),
                    )
                    res_resume = await resume_session.execute(stmt_resume)
                    remaining = res_resume.scalars().all()
                    # Reset "paused" tasks to "pending" for re-dispatch
                    for t in remaining:
                        if t.status == "paused":
                            t.status = "pending"
                            t.started_at = None
                    await resume_session.commit()

                current_nodes = [
                    TaskNode(
                        id=t.id,
                        plugin_name=t.plugin_name,
                        priority=t.priority,
                        depends_on=t.depends_on or [],
                        status="pending",
                        category=t.category or "",
                        steps=t.steps or [],
                    )
                    for t in remaining
                ]

            # Print summary
            completed = len(total_completed)
            failed = len(total_failed)
            console.print("\n[bold]Run complete:[/bold]")
            console.print(f"  ✅ Completed: {completed}")
            if failed > 0:
                console.print(f"  ❌ Failed: {failed}")
                # Show a concise per-task failure reason (first line only)
                for task_id, err in list(total_failed.items())[:5]:
                    short = err.splitlines()[0][:120] if err else "unknown error"
                    console.print(f"     [dim]{task_id[:8]}…[/dim] {short}")
                if len(total_failed) > 5:
                    console.print(f"     [dim]… and {len(total_failed) - 5} more[/dim]")
                # Give an actionable hint when the error is provider exhaustion
                sample_err = next(iter(total_failed.values()), "")
                if "All providers exhausted" in sample_err or "No agent executor" in sample_err:
                    console.print(
                        "\n[yellow]💡 No working providers found.[/yellow]\n"
                        "  • Run [bold]claw-forge pool-status[/bold] to see active providers\n"
                        "  • Add a valid API key to claw-forge.yaml (e.g. ANTHROPIC_API_KEY_1)\n"
                        "  • Or install the claude CLI for OAuth-based execution: "
                        "https://claude.ai/download"
                    )
            else:
                console.print("  🎉 All tasks succeeded!")

    # Pop CLAUDECODE once for the entire run — prevents the claude CLI
    # from refusing to start (it detects nesting via this env var).
    # Restored in finally so the parent session is unaffected.
    _saved_claudecode = os.environ.pop("CLAUDECODE", None)
    try:
        try:
            running_loop = asyncio.get_running_loop()
        except RuntimeError:
            running_loop = None

        if running_loop is not None:
            import concurrent.futures

            with concurrent.futures.ThreadPoolExecutor(max_workers=1) as pool:
                pool.submit(asyncio.run, main()).result()
        else:
            asyncio.run(main())
    finally:
        if _saved_claudecode is not None:
            os.environ["CLAUDECODE"] = _saved_claudecode


