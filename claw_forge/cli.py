def _resolve_latest_session(db_path: Path) -> str:
    """Read the most-recent session id from state.db; return '' if none found."""
    try:
        import sqlite3 as _sq
        if db_path.exists():
            with _sq.connect(str(db_path)) as conn:
                row = conn.execute(
                    "SELECT id FROM sessions ORDER BY created_at DESC LIMIT 1"
                ).fetchone()
                if row:
                    return str(row[0])
    except Exception:  # noqa: BLE001
        pass
    return ""


def _build_session_redirect_js(session_id: str) -> str:
    """Return inline JS that redirects the SPA to ?session=<id> if not already set."""
    if not session_id:
        return ""
    return (
        f"if(!new URLSearchParams(location.search).get('session')&&"
        f"!location.hash){{"
        f"history.replaceState(null,'',location.pathname+'?session={session_id}');}}"
    )


