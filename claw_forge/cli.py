def dev(
    ui_port: int = typer.Option(
        int(os.environ.get("CLAW_FORGE_UI_PORT", "5173")),
        "--ui-port",
        help="Port for the Vite dev server",
    ),
    state_port: int = typer.Option(
        int(os.environ.get("CLAW_FORGE_STATE_PORT", "8420")),
        "--state-port",
        help="Port for the state service API",
    ),
    project: str = typer.Option(".", "--project", "-p", help="Project directory."),
    open_browser: bool = typer.Option(True, "--open/--no-open", help="Open browser automatically"),
    session: str = typer.Option("", "--session", "-s", help="Session UUID to open on the board"),
    run_agents: bool = typer.Option(
        False, "--run/--no-run",
        help="Also launch the agent orchestrator (claw-forge run) alongside the dev servers.",
    ),
) -> None:
    """Start both the API (with hot-reload) and the Kanban UI (Vite dev server).

    This is the recommended way to develop locally — a single command that
    runs the FastAPI state service with uvicorn --reload and the Vite HMR
    dev server side by side.  Both restart automatically on file changes.

    Examples:

        # Start everything with defaults
        claw-forge dev

        # Custom ports
        claw-forge dev --state-port 9000 --ui-port 3000

        # Also run agents (orchestrator + UI + state service together)
        claw-forge dev --project /path/to/project --run
    """
    import shutil
    import signal
    import subprocess
    import threading
    import time
    import webbrowser

    project_path = Path(project).resolve()
    # Load .env from the project directory so API keys are available
    _load_env_file(project_path)

    # ── Validate prerequisites ──────────────────────────────────────────────
    ui_dir = Path(__file__).parent.parent / "ui"
    if not ui_dir.exists():
        console.print(
            "[red]ui/ source directory not found.[/red]\n"
            "[dim]`claw-forge dev` requires a source checkout.[/dim]"
        )
        raise typer.Exit(1) from None
    if not shutil.which("node"):
        console.print("[red]Node.js not found. Install from https://nodejs.org/[/red]")
        raise typer.Exit(1) from None

    node_modules = ui_dir / "node_modules"
    if not node_modules.exists():
        console.print("[yellow]Installing UI dependencies (npm install)…[/yellow]")
        subprocess.run(["npm", "install"], cwd=ui_dir, check=True)  # noqa: S603, S607

    # ── Prepare state service database ──────────────────────────────────────
    db_path = project_path / ".claw-forge" / "state.db"
    db_path.parent.mkdir(parents=True, exist_ok=True)
    db_url = f"sqlite+aiosqlite:///{db_path}"

    # ── Resolve session for display ─────────────────────────────────────────
    _session_id_dev = "(none — run `claw-forge run` to create a session)"
    try:
        import sqlite3 as _sqlite3_dev

        if db_path.exists():
            with _sqlite3_dev.connect(str(db_path)) as _conn:
                _row = _conn.execute(
                    "SELECT id FROM sessions ORDER BY created_at DESC LIMIT 1"
                ).fetchone()
                if _row:
                    _session_id_dev = _row[0]
    except Exception:  # noqa: BLE001
        pass

    url = f"http://localhost:{ui_port}"
    if session:
        url += f"/?session={session}"

    console.print("[bold green]🔥 claw-forge dev (API + UI hot-reload)[/bold green]")
    console.print(f"   UI:        [cyan]{url}[/cyan]  (Vite HMR)")
    console.print(f"   State API: [cyan]http://localhost:{state_port}[/cyan]  (uvicorn --reload)")
    console.print(f"   Database:  [dim]{db_path}[/dim]")
    console.print(f"   Session:   [yellow]{_session_id_dev}[/yellow]")
    if run_agents:
        console.print("   Agents:    [green]enabled (--run)[/green]")
    else:
        console.print("   Agents:    [dim]disabled — pass --run to launch orchestrator[/dim]")
    console.print("   Press [bold]Ctrl+C[/bold] to stop all servers\n")

    # ── Launch state service with --reload ──────────────────────────────────
    state_env = os.environ.copy()
    state_env["CLAW_FORGE_DB_URL"] = db_url
    state_proc = subprocess.Popen(  # noqa: S603, S607
        [
            "claw-forge", "state",
            "--project", str(project_path),
            "--port", str(state_port),
            "--reload",
        ],
        env=state_env,
    )

    # ── Launch Vite dev server ──────────────────────────────────────────────
    ui_env = os.environ.copy()
    ui_env["VITE_API_PORT"] = str(state_port)
    ui_env["VITE_WS_PORT"] = str(state_port)
    ui_proc = subprocess.Popen(  # noqa: S603, S607
        ["npm", "run", "dev", "--", "--port", str(ui_port), "--host"],
        cwd=ui_dir,
        env=ui_env,
    )

    # ── Optionally launch the agent orchestrator ─────────────────────────────
    run_proc: subprocess.Popen[bytes] | None = None
    if run_agents:
        # Give the state service a moment to start before the orchestrator connects
        time.sleep(2)
        run_env = os.environ.copy()
        run_env["CLAW_FORGE_STATE_PORT"] = str(state_port)
        run_proc = subprocess.Popen(  # noqa: S603, S607
            ["claw-forge", "run", "--project", str(project_path)],
            env=run_env,
        )

    # ── Open browser after a short delay ────────────────────────────────────
    if open_browser:
        def _open_browser() -> None:
            time.sleep(3)
            webbrowser.open(url)
        threading.Thread(target=_open_browser, daemon=True).start()

    # ── Wait for either process to exit or Ctrl+C ──────────────────────────
    def _shutdown(*_args: object) -> None:
        procs = [p for p in (run_proc, ui_proc, state_proc) if p is not None]
        for proc in procs:
            if proc.poll() is None:
                proc.terminate()

    signal.signal(signal.SIGINT, _shutdown)
    signal.signal(signal.SIGTERM, _shutdown)

    try:
        # Wait for any watched process to finish
        watched = [p for p in (run_proc, ui_proc, state_proc) if p is not None]
        while all(p.poll() is None for p in watched):
            time.sleep(0.5)
    except KeyboardInterrupt:
        pass
    finally:
        _shutdown()
        for p in [p for p in (run_proc, ui_proc, state_proc) if p is not None]:
            p.wait()
        console.print("\n[dim]All servers stopped.[/dim]")


@app.command()
def merge(
    branch: str = typer.Argument(None, help="Branch to squash-merge (e.g. feat/user-auth)"),
    project: str = typer.Option(".", "--project", "-p", help="Project directory."),
    target: str = typer.Option("main", "--target", "-t", help="Target branch to merge into."),
) -> None:
    """Squash-merge a feature branch to the target branch.

    Used with merge_strategy: manual to control when features land on main.
    If no branch is specified, lists branches with the configured prefix.

    Examples:

        # Merge a specific branch
        claw-forge merge feat/user-auth

        # List ready feature branches
        claw-forge merge

        # Merge into a custom target
        claw-forge merge feat/user-auth --target develop
    """
    import subprocess as sp

    project_path = Path(project).resolve()

    if branch is None:
        # List feature branches
        try:
            result = sp.run(
                ["git", "branch", "--list", "feat/*"],
                cwd=project_path, capture_output=True, text=True, check=True,
            )
            branches = [
                b.strip().lstrip("* ")
                for b in result.stdout.strip().splitlines()
                if b.strip()
            ]
            if not branches:
                console.print("[dim]No feature branches found.[/dim]")
                return
            console.print("[bold]Feature branches:[/bold]")
            for b in branches:
                console.print(f"  \u2022 {b}")
            console.print("\n[dim]Run: claw-forge merge <branch>[/dim]")
        except sp.CalledProcessError:
            console.print("[red]Not a git repository or git not available.[/red]")
        return

    from claw_forge.git.merge import squash_merge

    result = squash_merge(project_path, branch, target)
    if result["merged"]:
        _hash = result['commit_hash']
        console.print(f"[green]\u2713 Merged {branch} \u2192 {target} ({_hash})[/green]")
    else:
        console.print(f"[red]\u2717 Merge failed: {result.get('error', 'unknown')}[/red]")


@app.command()
def fix(
    description: str | None = typer.Argument(
        None, help="Bug description (use --report for structured reports)"
    ),
    report: str | None = typer.Option(
        None, "--report", "-r",
        help="Path to bug_report.md (generated by /create-bug-report in Claude Code).",
    ),
    project: str = typer.Option(".", "--project", "-p", help="Project directory."),
    model: str = typer.Option(
        "claude-sonnet-4-6", "--model", "-m",
        help=(
            "Model to use. Supported formats:\n"
            "  claude-sonnet-4-6             bare model (pool picks provider)\n"
            "  anthropic-proxy-1/claude-opus-4-6    pin to specific provider\n"
            "  sonnet                               alias from model_aliases in config"
        ),
    ),
    branch: bool = typer.Option(
        True, "--branch/--no-branch",
        help="Create a fix/<slug> git branch before applying the fix.",
    ),
    config: str = typer.Option(
        "claw-forge.yaml", "--config", "-c",
        help="Path to claw-forge.yaml config file.",
    ),
) -> None:
    """Fix a bug using reproduce-first (RED→GREEN) protocol.

    Writes a regression test that reproduces the bug first, then fixes the root
    cause and verifies the full test suite still passes.

    Examples:

        # Fix from a description
        claw-forge fix 'users get 500 on login with uppercase email'

        # Fix from a structured bug report
        claw-forge fix --report bug_report.md

        # Fix without creating a branch
        claw-forge fix --report bug_report.md --no-branch

        # Fix with a specific model
        claw-forge fix 'search returns empty results' --model claude-opus-4-6
    """
    import re
    import subprocess

    from claw_forge.bugfix.report import BugReport
    from claw_forge.plugins.base import PluginContext
    from claw_forge.plugins.bugfix import BugFixPlugin

    cfg_fix = _load_config(config)
    resolved = resolve_model(model, cfg_fix)
    if resolved.alias_resolved:
        console.print(f"[dim]Model alias '{model}' → {resolved.model_id}[/dim]")
    if resolved.provider_hint:
        console.print(f"[dim]Provider pinned: {resolved.provider_hint}[/dim]")
    model = resolved.model_id

    if report:
        report_path = Path(report)
        if not report_path.exists():
            console.print(f"[red]Report file not found: {report_path}[/red]")
            raise typer.Exit(1) from None
        bug = BugReport.from_file(report_path)
    elif description:
        bug = BugReport.from_description(description)
    else:
        console.print("[red]Error: provide a description or --report[/red]")
        console.print("  claw-forge fix 'users get 500 on login'")
        console.print("  claw-forge fix --report bug_report.md")
        raise typer.Exit(1) from None

    console.print(f"[bold]🐛 Bug:[/bold] {bug.title}")

    if branch:
        slug = re.sub(r"[^a-z0-9]+", "-", bug.title.lower()).strip("-")[:50]
        branch_name = f"fix/{slug}"
        try:
            subprocess.run(  # noqa: S603, S607
                ["git", "checkout", "-b", branch_name],
                cwd=project,
                check=True,
                capture_output=True,
            )
            console.print(f"[dim]Created branch: {branch_name}[/dim]")
        except subprocess.CalledProcessError:
            console.print(
                f"[yellow]⚠ Could not create branch {branch_name!r} (continuing)[/yellow]"
            )

    ctx = PluginContext(
        project_path=project,
        session_id="bugfix",
        task_id=f"fix-{bug.title[:40]}",
        metadata={"bug_report": bug},
    )

    console.print("[cyan]Running bug-fix agent…[/cyan]")
    result = asyncio.run(BugFixPlugin().execute(ctx))

    if result.success:
        console.print("[bold green]✅ Bug fix complete[/bold green]")
        if result.files_modified:
            console.print(f"   Files modified: {', '.join(result.files_modified)}")
        if result.output:
            console.print(result.output[:2000])
    else:
        console.print(f"[red]Fix failed: {result.output}[/red]")
        raise typer.Exit(1) from None


