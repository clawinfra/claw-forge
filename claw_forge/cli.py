def _scaffold_config(config_path: str) -> bool:
    """Create claw-forge.yaml + .env.example if they don't exist.

    Returns True if config was newly created, False if it already existed.
    """
    path = Path(config_path)
    created = False
    if not path.exists():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(_DEFAULT_CONFIG_YAML)
        console.print(f"[green]✓ Created {path}[/green]  (edit providers as needed)")
        created = True
    env_example = path.parent / ".env.example"
    if not env_example.exists():
        env_example.write_text(_DEFAULT_ENV_EXAMPLE)
        console.print(f"[green]✓ Created {env_example}[/green]  (copy to .env and fill keys)")
    env_file = path.parent / ".env"
    if not env_file.exists():
        console.print(
            "[yellow]⚠  No .env found — copy .env.example → .env and add your API keys[/yellow]"
        )
    return created


def _load_config(config_path: str, *, auto_scaffold: bool = False) -> dict[str, Any]:
    """Load YAML config and expand ${ENV_VAR} placeholders from environment.

    When auto_scaffold=True (used by ``claw-forge init``), if the config file
    does not exist it is auto-created along with .env.example. For all other
    commands, a missing explicit --config path is still treated as an error.
    """
    path = Path(config_path)
    if not path.exists():
        if auto_scaffold:
            _scaffold_config(config_path)
        else:
            console.print(f"[red]Config not found: {path}[/red]")
            console.print(
                "[dim]Run [bold]claw-forge init[/bold] to create a default claw-forge.yaml[/dim]"
            )
            raise typer.Exit(1) from None
    # Auto-load .env file if present alongside the config
    _load_env_file(path.parent)
    raw = yaml.safe_load(path.read_text())
    return _expand_env_vars(raw)  # type: ignore[return-value]


